/**
 * `<MenuItem />` component display state
 */
export enum MenuItemVariantState {
  /**
   * Sets `<MenuItem />` as disabled
   */
  disabled = 'disabled',
  /**
   * Applies actives style to the element inside `<MenuItem />`
   */
  active = 'active',
  /**
   * Applies focus style to the element inside `<MenuItem />`
   */
  focus = 'focus',
}
