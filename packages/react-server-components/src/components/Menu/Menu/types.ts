/**
 * Direction for the `<MenuItem />` components inside `<Menu />`.
 */
export enum MenuVariantDirection {
  /**
   * Vertical menu (default)
   */
  vertical = 'menu-vertical',
  /**
   * Horizontal menu
   */
  horizontal = 'menu-horizontal',
}

/**
 * `<Menu />` component size
 */
export enum MenuVariantSize {
  /**
   * Large menu
   */
  lg = 'menu-lg',
  /**
   * Medium menu (default)
   */
  md = 'menu-md',
  /**
   * Small menu
   */
  sm = 'menu-sm',
  /**
   * Extra small menu
   */
  xs = 'menu-xs',
}
